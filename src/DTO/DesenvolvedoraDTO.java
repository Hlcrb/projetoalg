package DTO;

public class DesenvolvedoraDTO {

    private String nome_desen;
    private int codigo;

    /**
     * @return the nome_desen
     */
    public String getNome_desen() {
        return nome_desen;
    }

    /**
     * @param nome_desen the nome_desen to set
     */
    public void setNome_desen(String nome_desen) {
        this.nome_desen = nome_desen;
    }

    /**
     * @return the codigo
     */
    public int getCodigo() {
        return codigo;
    }

    /**
     * @param codigo the codigo to set
     */
    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }

}
